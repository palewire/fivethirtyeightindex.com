"""Extract and save ai2html graphics embedded in article HTML.

This runs parallel to :mod:`fakethirtyeight.images`: the reference stage
walks ``data/articles/**/*.html.gz`` and finds ai2html graphics, while the
download stage materializes each unique HTML graphic into ``data/ai2html/``.

FiveThirtyEight used two ai2html shapes:

* Older articles sometimes include the generated ai2html markup inline,
  usually under a ``class="ai2html"`` wrapper and a ``Generated by ai2html``
  comment.
* Newer ABC-era articles usually include an empty ``ai2html_block_*`` div and
  a ``new pym.Parent(...)`` script that points at a standalone
  ``?ai2html=<wp-content/uploads/...html>`` URL.
"""

